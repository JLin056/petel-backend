CREATE TABLE PETEL_ACCOUNTS (
      ID            VARCHAR2(5)     PRIMARY KEY,
      EMAIL         VARCHAR2(255)   NOT NULL UNIQUE,
      PASSWORD      VARCHAR2(255)   NOT NULL,
      ROLE          VARCHAR2(20)    NOT NULL,
      STATUS        VARCHAR2(20)    NOT NULL,
	  TOKEN_VERSION number 			NOT NULL
  );

  CREATE TABLE PETEL_USERS (
      ID              VARCHAR2(5)     PRIMARY KEY,
      ACCOUNT_ID      VARCHAR2(5)     NOT NULL,
      NAME            NVARCHAR2(50),
      PHONE           VARCHAR2(10 BYTE),
      AVATAR_MEDIA_ID VARCHAR2(10 BYTE)
  );

  CREATE TABLE PETEL_SELLERS (
      ID              VARCHAR2(5)     PRIMARY KEY,
      ACCOUNT_ID      VARCHAR2(5)     NOT NULL,
      NAME            NVARCHAR2(400)  NOT NULL,
      BUSINESS_CODE   NVARCHAR2(100)  NOT NULL,
      AVATAR_MEDIA_ID NUMBER          NOT NULL
  );

  CREATE TABLE PETEL_PROPERTY (
      ID              VARCHAR2(5)     PRIMARY KEY,
      SELLER_ID       VARCHAR2(5),
      NAME            NVARCHAR2(400)  NOT NULL,
      TEL             VARCHAR2(50)    NOT NULL,
      POSTAL_CODE     VARCHAR2(10)    NOT NULL,
      ADDRESS         NVARCHAR2(400)  NOT NULL,
      BANK_ACCOUNT    VARCHAR2(100)   NOT NULL,
      INFO            NVARCHAR2(1000) NOT NULL,
      CHECK_NOTICE    NVARCHAR2(1000) NOT NULL,
      PET_NOTICE      NVARCHAR2(1000),
      PROPERTY_NOTICE NVARCHAR2(1000)
  );
  
  create table PETEL_ORDERS (
  ID varchar2(10) primary key,
  USER_ID varchar2(10) not null,
  PROPERTY_ID varchar2(10) not null,
  PAYMENT_ID varchar2(10) not null,
  HOTEL_CHARGES number not null,
  CHECK_IN varchar2(20) not null, -- 'yyyy-MM-dd'
  CHECK_OUT varchar2(20) not null, -- 'yyyy-MM-dd'
  STATUS nvarchar2(20) not null,
  GUEST char(1) default 'y' not null,
  GUEST_NAME nvarchar2(20),
  GUEST_PHONE varchar2(15),
  NOTE nvarchar2(200),
  CREATED_AT timestamp not null,
  UPDATED_AT timestamp
);

create table PETEL_PAYMENT (
  ID varchar2(10) primary key,
  NAME nvarchar2(20) not null
);

